///aqui fica a redução dos tickets

let ticket1 = document.getElementById('ticket1');
let ho1 = document.getElementById('ho1');

function aperte1() {// essa função foi criadapara dar ação 

  let valor1 = parseInt(ticket1.innerHTML);// Obtém o valor atual do botão
  if (valor1 > 0) {// Verifica se o valor é maior que 0
    valor1--;// Reduz o valor em 1
    ticket1.innerHTML = valor1;// Atualiza o texto do botão com o novo valor
  } 
  if(valor1 == 0){
    ho1.style.backgroundColor = 'gray';
    
  }

}
ho1.addEventListener("click", aperte1);

